
package UriProblemSolveing;

import java.util.Scanner;


public class Uri1933 {
    public static void main(String[] args) {
        Scanner input = new Scanner (System.in);
        int a,b;
        a = input.nextInt();
        b = input.nextInt();
        if(a==b)
        {
            System.out.println(a);
        }
        else if(a>b)
        {
            System.out.println(a);
        }
        else
        {
            System.out.println(b);
        }
           
    }
    
}
